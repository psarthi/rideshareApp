package com.parift.rideshare.model.serviceprovider.dto;

public class UserRidesStats {
	
	private int lastWeekCount;
	private int thisWeekCount;
	private int lastMonthCount;
	private int thisMonthCount;
	
	public int getLastWeekCount() {
		return lastWeekCount;
	}
	public void setLastWeekCount(int lastWeekCount) {
		this.lastWeekCount = lastWeekCount;
	}
	public int getThisWeekCount() {
		return thisWeekCount;
	}
	public void setThisWeekCount(int thisWeekCount) {
		this.thisWeekCount = thisWeekCount;
	}
	public int getLastMonthCount() {
		return lastMonthCount;
	}
	public void setLastMonthCount(int lastMonthCount) {
		this.lastMonthCount = lastMonthCount;
	}
	public int getThisMonthCount() {
		return thisMonthCount;
	}
	public void setThisMonthCount(int thisMonthCount) {
		this.thisMonthCount = thisMonthCount;
	}

	

}
